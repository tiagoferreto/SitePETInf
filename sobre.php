            <h3>Histórico</h3>
            <p>O PET – Programa de Educação Tutorial – foi idealizado pelo Prof. Dr. Cláudio de Moura Castro e implantado pela CAPES em 1979, tendo em vista o melhor preparo dos alunos da graduação.</p>

            <p>Até 1996, o programa estava expandindo-se, porém a CAPES anunciou futuros cortes. No ano de 1997, o governo federal anunciou uma série de cortes na educação, devido a crise, que afetariam inclusive o programa. Desta forma, os bolsistas e tutores iniciaram mobilizações em prol do PET (InterPETs estaduais, SulPET, SudestePET e o encontro nacional dos grupos PET – ENAPET).</p>

            <p>A partir de 2000, o grupo passou a ser vinculado ao SESu/MEC, porém, neste mesmo período, as ameaças para a extinção do grupo eram grandes. Em 2003, o PET foi rediscutido com seus protagonistas: CENAPET (Comissão Executiva Nacional), SESu/MEC, FORGRAD (Fórum dos Pró-Reitores de Graduação das IES do Brasil). Hoje o PET conta com 3177 bolsistas de 298 grupo em todo Brasil.</p>

            <p>O "PET-Inf" foi criado na PUCRS em novembro de 1991, tendo como primeiro tutor o professor Álvaro Guarda.</p>
